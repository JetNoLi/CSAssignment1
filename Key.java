public class Key{
        private int stage;
        private int day;
        private int time;

        public Key(int stage, int day, int time){
                this.stage = stage;
                this.day = day;
                this.time = time;
                }

        public int getStage(){
                return stage;
                }

        public int getDay(){
                return day;
                }

        public int getTime(){
                return time;
                }

        }

